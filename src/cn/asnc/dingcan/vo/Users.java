package cn.asnc.dingcan.vo;

public class Users {
	private int u_no;
	private String u_name;
	private String u_pwd;
	private String u_tell;
	private int u_role;
	private double u_money;
	
	public int getU_no() {
		return u_no;
	}
	public void setU_no(int u_no) {
		this.u_no = u_no;
	}
	public String getU_name() {
		return u_name;
	}
	public void setU_name(String u_name) {
		this.u_name = u_name;
	}
	public String getU_pwd() {
		return u_pwd;
	}
	public void setU_pwd(String u_pwd) {
		this.u_pwd = u_pwd;
	}
	public String getU_tell() {
		return u_tell;
	}
	public void setU_tell(String u_tell) {
		this.u_tell = u_tell;
	}
	public int getU_role() {
		return u_role;
	}
	public void setU_role(int u_role) {
		this.u_role = u_role;
	}
	public double getU_money() {
		return u_money;
	}
	public void setU_money(double u_money) {
		this.u_money = u_money;
	}
	
}
